class Cell{

    setLeft(left){
        this.left = left;
    }

    getLeft(){
        return this.left;
    }

    setTop(top){
        this.top = top;
    }

    getTop(){
        return this.top;
    }

    setWidth(width){
        this.width = width;
    }

    getWidth(){
        return this.width;
    }

    setHeight(height){
        this.height = height;
    }

    getHeight(){
        return this.height;
    }

    setHAlign(hAlign){
        this.hAlign = hAlign;
    }

    setVAlign(vAlign){
        this.vAlign = vAlign;
    }

    setFont(font){
        this.font = font;
    }

    setFontSize(fontSize){
        this.fontSize = fontSize;
    }

    setBold(bold){
        this.bold = bold;
    }

    setText(text){
        this.text = text;
    }

    setFormula(formula){
        this.formula = formula;
    }

    setTopBorder(topBorder){
        this.topBorder = topBorder;
    }

    setRightBorder(rightBorder){
        this.rightBorder = rightBorder;
    }

    setBottomBorder(bottomBorder){
        this.bottomBorder = bottomBorder;
    }

    setLeftBorder(leftBorder){
        this.leftBorder = leftBorder;
    }

    setColSpan(colSpan){
        this.colSpan = colSpan;
    }

    setRowSpan(rowSpan){
        this.rowSpan = rowSpan;
    }

}

export default Cell;